FleetApplyStatus FleetControllerApplyBundle(
    FleetController* fc, const uint8_t* body,
    int body_len) {
  std::string outer(reinterpret_cast<const char*>(body),
                    body_len);
  std::string signed_b64, sig_b64, outer_fleet;
  int64_t outer_version = 0;
  if (!ExtractString(outer, "signed_body_b64",
                     &signed_b64) ||
      !ExtractString(outer, "signature_b64", &sig_b64) ||
      !ExtractString(outer, "fleet_id", &outer_fleet) ||
      !ExtractInt(outer, "version", &outer_version)) {
    return FleetApplyStatus::kParseError;
  }
  if (outer_fleet != fc->config.fleet_id) {
    return FleetApplyStatus::kFleetMismatch;
  }
  if (outer_version <=
      fc->applied_version.load(
          std::memory_order_acquire)) {
    return FleetApplyStatus::kStaleVersion;
  }
  std::vector<uint8_t> signed_bytes, sig;
  if (!B64Decode(signed_b64, &signed_bytes) ||
      !B64Decode(sig_b64, &sig)) {
    return FleetApplyStatus::kParseError;
  }
  std::vector<uint8_t> pk;
  if (!B64Decode(fc->config.signing_pubkey_b64, &pk) ||
      pk.size() !=
          crypto_sign_ed25519_PUBLICKEYBYTES) {
    return FleetApplyStatus::kBadSignature;
  }
  if (sig.size() != crypto_sign_ed25519_BYTES) {
    return FleetApplyStatus::kBadSignature;
  }
  if (crypto_sign_ed25519_verify_detached(
          sig.data(), signed_bytes.data(),
          signed_bytes.size(), pk.data()) != 0) {
    return FleetApplyStatus::kBadSignature;
  }

  std::string inner(
      reinterpret_cast<const char*>(signed_bytes.data()),
      signed_bytes.size());

  // Inner fleet_id + version must match the outer
  // fields (envelope integrity check).
  std::string inner_fleet;
  int64_t inner_version = 0;
  if (!ExtractString(inner, "fleet_id", &inner_fleet) ||
      !ExtractInt(inner, "version", &inner_version) ||
      inner_fleet != outer_fleet ||
      inner_version != outer_version) {
    return FleetApplyStatus::kParseError;
  }

  FleetApplyStatus s = ApplyInner(fc, inner);
  if (s != FleetApplyStatus::kOk) return s;

  if (ApplyRevocations(fc, inner)) {
    fc->self_revoked.store(true,
                           std::memory_order_release);
    fc->applied_version.store(
        inner_version, std::memory_order_release);
    return FleetApplyStatus::kRevoked;
  }

  fc->applied_version.store(inner_version,
                            std::memory_order_release);
  return FleetApplyStatus::kOk;
}

namespace {

void RunLoop(FleetController* fc) {
  std::mt19937 rng{static_cast<uint32_t>(
      std::chrono::steady_clock::now()
          .time_since_epoch()
          .count())};
  std::uniform_int_distribution<int> jitter(-10, 10);
  ParsedUrl url;
  std::string full = fc->config.url;
  if (!full.empty() && full.back() == '/') {
    full.pop_back();
  }
  full += "/api/v1/policy";
  if (!ParseHttpsUrl(full, &url)) {
    spdlog::error(
        "fleet controller: bad url '{}' — disabling",
        fc->config.url);
    return;
  }

  while (fc->running.load(std::memory_order_acquire)) {
    HttpsResponse resp;
    int64_t since = fc->applied_version.load(
        std::memory_order_acquire);
    if (HttpsGet(fc->config, url, since, &resp)) {
      if (resp.status == 304) {
        fc->last_status.store(FleetApplyStatus::kOk,
                              std::memory_order_relaxed);
      } else if (resp.status == 200) {
        auto st = FleetControllerApplyBundle(
            fc,
            reinterpret_cast<const uint8_t*>(
                resp.body.data()),
            static_cast<int>(resp.body.size()));
        fc->last_status.store(st,
                              std::memory_order_relaxed);
        if (st == FleetApplyStatus::kOk ||
            st == FleetApplyStatus::kRevoked) {
          WriteCache(fc->config.bundle_cache_path,
                     resp.body);
        }
        if (st == FleetApplyStatus::kRevoked) {
          spdlog::error(
              "fleet: this relay's id is revoked — "
              "self-terminating");
          // Signal to the main server loop via the
          // self_revoked flag; the loop checks every
          // iteration.
        }
      } else {
        fc->last_status.store(
            FleetApplyStatus::kNetworkError,
            std::memory_order_relaxed);
      }
    } else {
      fc->last_status.store(
          FleetApplyStatus::kNetworkError,
          std::memory_order_relaxed);
    }

    int sleep_s = fc->config.poll_interval_secs +
                  jitter(rng);
    if (sleep_s < 10) sleep_s = 10;
    for (int i = 0; i < sleep_s * 10 &&
                    fc->running.load(
                        std::memory_order_acquire);
         i++) {
      usleep(100000);
    }
  }
}

}  // namespace

bool FleetControllerStart(FleetController* fc,
                          HdPeerRegistry* hd_peers,
                          const FleetControllerConfig&
                              config) {
  fc->config = config;
  fc->hd_peers = hd_peers;
  // Load last-known-good if present so the relay has
  // fleet state even before the first poll succeeds.
  std::string cached;
  if (!config.bundle_cache_path.empty() &&
      ReadCache(config.bundle_cache_path, &cached)) {
    auto st = FleetControllerApplyBundle(
        fc,
        reinterpret_cast<const uint8_t*>(cached.data()),
        static_cast<int>(cached.size()));
    if (st != FleetApplyStatus::kOk &&
        st != FleetApplyStatus::kRevoked) {
      spdlog::warn(
          "fleet: cached bundle apply failed ({})",
          static_cast<int>(st));
    }
  }
  fc->running.store(1, std::memory_order_release);
  fc->thread = std::thread(RunLoop, fc);
  return true;
}

void FleetControllerStop(FleetController* fc) {
  fc->running.store(0, std::memory_order_release);
  if (fc->thread.joinable()) fc->thread.join();
}

void FleetControllerGetRevokedPeers(
    const FleetController* fc,
    std::vector<std::string>* out) {
  std::lock_guard lk(fc->revoked_mu);
  *out = fc->revoked_peers;
}

